/**
 * Broad types are:
 * - regular
 * - infinitive
 * - compound
 * - past participle
 * THROW ERROR IF MULTIPLE TYPES
 * even though we expect this to occur at times, we would like user to manually resolve
 * the unexpected case rather than getting bad data.
 */
export const getBroadType = ({ text }) => {
    return text.includes();
};